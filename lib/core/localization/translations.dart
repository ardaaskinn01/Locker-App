import 'package:flutter_riverpod/flutter_riverpod.dart';
import '../../features/onboarding/onboarding_provider.dart';

class Translations {
  final Map<String, String> _localizedValues;

  Translations(this._localizedValues);

  String get(String key) {
    return _localizedValues[key] ?? key;
  }

  String tr(String key, [Map<String, String>? params]) {
    String value = _localizedValues[key] ?? key;
    if (params != null) {
      params.forEach((k, v) {
        value = value.replaceAll('{$k}', v);
      });
    }
    return value;
  }
}

final translationProvider = Provider<Translations>((ref) {
  final langCode = ref.watch(languageProvider);
  if (langCode == 'en') {
    return Translations(_en);
  }
  return Translations(_tr);
});

